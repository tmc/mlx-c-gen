			fmt.Fprintln(b, "\t\tif value < 0 {")
			fmt.Fprintf(b, "\t\t\treturn value, lastCError(%q)\n", fn.Name)
			fmt.Fprintln(b, "\t\t}")
			fmt.Fprintln(b, "\t\treturn value, nil")
