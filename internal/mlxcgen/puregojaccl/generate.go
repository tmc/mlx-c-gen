// Options controls generated JACCL purego bindings.
type Options struct {
	PackageName string
	TargetName  string
	ToolName    string
}

// Generate returns formatted Go files for the JACCL purego package.
func Generate(lock *apilock.Lock, opts Options) (map[string][]byte, error) {
	if lock == nil {
		return nil, fmt.Errorf("generate purego jaccl: nil api lock")
	}
	if opts.PackageName == "" {
		opts.PackageName = "jacclc"
	}
	if opts.TargetName == "" {
		opts.TargetName = "jacclc"
	}
	if opts.ToolName == "" {
		opts.ToolName = "mlx-c-gen-purego-jaccl"
	}
	target, ok := lock.Targets[opts.TargetName]
	if !ok {
		return nil, fmt.Errorf("generate purego jaccl: target %q not found", opts.TargetName)
	}
	if err := validateTarget(target); err != nil {
		return nil, err
	}

	files := map[string]string{
		"doc.gen.go":       genDoc(opts),
		"lib.gen.go":       genLib(opts),
		"types.gen.go":     genTypes(opts, target),
		"functions.gen.go": genFunctions(opts, target),
	}
	out := make(map[string][]byte, len(files))
	for name, src := range files {
		formatted, err := format.Source([]byte(src))
		if err != nil {
			return nil, fmt.Errorf("format %s: %w", name, err)
		}
		out[name] = formatted
	}
	return out, nil
}

func validateTarget(target apilock.Target) error {
	structs := map[string]bool{}
	for _, st := range target.Structs {
		if !st.Opaque {
			return fmt.Errorf("generate purego jaccl: struct %s is not opaque", st.Name)
		}
		structs[st.Name] = true
	}
	for _, name := range []string{"mlx_jaccl_group", "mlx_jaccl_config"} {
		if !structs[name] {
			return fmt.Errorf("generate purego jaccl: missing struct %s", name)
		}
	}
	enums := map[string]bool{}
	for _, enum := range target.Enums {
		enums[enum.Name] = true
	}
	if !enums["mlx_jaccl_dtype"] {
		return fmt.Errorf("generate purego jaccl: missing enum mlx_jaccl_dtype")
	}
	for _, fn := range target.Functions {
		if !strings.HasPrefix(fn.Name, "mlx_jaccl_") {
			return fmt.Errorf("generate purego jaccl: unexpected function %s", fn.Name)
		}
	}
	return nil
}

func header(opts Options) string {
	return fmt.Sprintf("// Code generated by %s from codegen/mlxc-capi.lock.json; DO NOT EDIT.\n\n", opts.ToolName)
}

func genDoc(opts Options) string {
	return header(opts) + fmt.Sprintf(`// Package %s provides purego bindings for libjacclc.
package %s
`, opts.PackageName, opts.PackageName)
}

func genLib(opts Options) string {
	return header(opts) + fmt.Sprintf(`package %s

import (
	"fmt"
	"os"
	"path/filepath"
	"runtime"
	"sync"
	"sync/atomic"

	"github.com/ebitengine/purego"
)

const (
	envLibraryPath    = "MLX_C_JACCLC_LIB_PATH"
	envLibraryPathAlt = "MLX_JACCLC_LIB_PATH"
)

var (
	loadMu            sync.Mutex
	jacclcLib         uintptr
	jacclcLoaded      atomic.Uintptr
	loadedLibraryPath string
)

// LibraryPath returns the loaded libjacclc path.
func LibraryPath() string {
	loadMu.Lock()
	defer loadMu.Unlock()
	return loadedLibraryPath
}

// Load loads libjacclc from the environment or standard library locations.
func Load() error {
	return loadPath("")
}

// LoadPath loads libjacclc from path. If path is a directory, it is expected
// to contain libjacclc and its sibling dependencies.
func LoadPath(path string) error {
	return loadPath(path)
}

func ensureLoaded() error {
	if jacclcLoaded.Load() != 0 {
		return nil
	}
	return Load()
}

func loadPath(path string) error {
	loadMu.Lock()
	defer loadMu.Unlock()
	if jacclcLib != 0 {
		if path == "" || sameLibraryPath(path, loadedLibraryPath) {
			return nil
		}
		return fmt.Errorf("jacclc already loaded from %%s", loadedLibraryPath)
	}
	if path == "" {
		resolved, err := resolveLibraryPath()
		if err != nil {
			return err
		}
		path = resolved
	} else {
		path = resolveLibraryOverride(path)
	}
	if err := preloadSiblingLibraries(path); err != nil {
		return err
	}
	lib, err := purego.Dlopen(path, purego.RTLD_NOW|purego.RTLD_GLOBAL)
	if err != nil {
		return fmt.Errorf("load %%s: %%w", path, err)
	}
	if err := registerJACCLFunctions(lib); err != nil {
		return err
	}
	jacclcLib = lib
	loadedLibraryPath = path
	jacclcLoaded.Store(lib)
	return nil
}

func sameLibraryPath(path, loaded string) bool {
	path = resolveLibraryOverride(path)
	if path == loaded {
		return true
	}
	absPath, err1 := filepath.Abs(path)
	absLoaded, err2 := filepath.Abs(loaded)
	return err1 == nil && err2 == nil && absPath == absLoaded
}

func resolveLibraryPath() (string, error) {
	for _, name := range []string{envLibraryPath, envLibraryPathAlt} {
		if value := os.Getenv(name); value != "" {
			return resolveLibraryOverride(value), nil
		}
	}

	libName := libraryFileName("libjacclc")
	for _, dir := range sourceSearchDirs() {
		candidate := filepath.Join(dir, libName)
		if _, err := os.Stat(candidate); err == nil {
			return candidate, nil
		}
	}

	paths := []string{
		filepath.Join(os.Getenv("HOME"), "lib", libName),
		filepath.Join(os.Getenv("HOME"), ".local", "lib", libName),
		"/usr/local/lib/" + libName,
	}
	if runtime.GOOS == "darwin" {
		paths = append(paths, "/opt/homebrew/lib/"+libName)
	}
	for _, path := range paths {
		if _, err := os.Stat(path); err == nil {
			return path, nil
		}
	}
	return "", fmt.Errorf("libjacclc not found; set %%s to a libjacclc file or directory", envLibraryPath)
}

func sourceSearchDirs() []string {
	_, file, _, ok := runtime.Caller(0)
	if !ok {
		return nil
	}
	dir := filepath.Dir(file)
	for depth := 0; depth < 10; depth++ {
		if _, err := os.Stat(filepath.Join(dir, "go.mod")); err == nil {
			return []string{
				filepath.Join(dir, "build-shared"),
				filepath.Join(dir, "build-Release"),
				filepath.Join(dir, "build-Debug"),
				filepath.Join(dir, "build"),
				filepath.Join(dir, "install", "lib"),
			}
		}
		parent := filepath.Dir(dir)
		if parent == dir {
			return nil
		}
		dir = parent
	}
	return nil
}

func resolveLibraryOverride(path string) string {
	info, err := os.Stat(path)
	if err != nil || !info.IsDir() {
		return path
	}
	return filepath.Join(path, libraryFileName("libjacclc"))
}

func libraryFileName(base string) string {
	switch runtime.GOOS {
	case "darwin":
		return base + ".dylib"
	case "linux":
		return base + ".so"
	default:
		return base
	}
}

func preloadSiblingLibraries(path string) error {
	dir := filepath.Dir(path)
	for _, base := range []string{"libjaccl", "libmlx"} {
		candidate := filepath.Join(dir, libraryFileName(base))
		if _, err := os.Stat(candidate); err != nil {
			continue
		}
		if _, err := purego.Dlopen(candidate, purego.RTLD_NOW|purego.RTLD_GLOBAL); err != nil {
			return fmt.Errorf("preload %%s: %%w", candidate, err)
		}
	}
	return nil
}

func registerLibFunc(fptr any, lib uintptr, name string) (err error) {
	defer func() {
		if r := recover(); r != nil {
			err = fmt.Errorf("register %%s: %%v", name, r)
		}
	}()
	purego.RegisterLibFunc(fptr, lib, name)
	return nil
}
`, opts.PackageName)
}

func genTypes(opts Options, target apilock.Target) string {
	var b bytes.Buffer
	fmt.Fprint(&b, header(opts))
	fmt.Fprintf(&b, "package %s\n\n", opts.PackageName)
	fmt.Fprintln(&b, "import \"unsafe\"")
	fmt.Fprintln(&b)

	structs := append([]apilock.Struct(nil), target.Structs...)
	sort.Slice(structs, func(i, j int) bool { return structs[i].Name < structs[j].Name })
	for _, st := range structs {
		name := goStructName(st.Name)
		fmt.Fprintf(&b, "// %s wraps %s.\n", name, st.Name)
		fmt.Fprintf(&b, "type %s struct {\n\tctx unsafe.Pointer\n}\n\n", name)
		fmt.Fprintf(&b, "// Ptr returns the underlying pointer.\n")
		fmt.Fprintf(&b, "func (t %s) Ptr() unsafe.Pointer {\n\treturn t.ctx\n}\n\n", name)
		fmt.Fprintf(&b, "// IsNil reports whether the underlying pointer is nil.\n")
		fmt.Fprintf(&b, "func (t %s) IsNil() bool {\n\treturn t.ctx == nil\n}\n\n", name)
		fmt.Fprintf(&b, "func (t %s) handle() unsafe.Pointer {\n\treturn t.ctx\n}\n\n", name)
		fmt.Fprintf(&b, "func %sFromHandle(handle unsafe.Pointer) %s {\n\treturn %s{ctx: handle}\n}\n\n",
			lowerFirst(name), name, name)
	}

	for _, enum := range target.Enums {
		if enum.Name != "mlx_jaccl_dtype" {
			continue
		}
		fmt.Fprintln(&b, "// DType is a JACCL element type.")
		fmt.Fprintln(&b, "type DType int32")
		fmt.Fprintln(&b)
		fmt.Fprintln(&b, "const (")
		for _, value := range enum.Values {
			fmt.Fprintf(&b, "\t%s DType = %d\n", goEnumValueName(value.Name), value.Value)
		}
		fmt.Fprintln(&b, ")")
		fmt.Fprintln(&b)
	}
	return b.String()
}

