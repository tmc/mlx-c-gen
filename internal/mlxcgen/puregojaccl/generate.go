	fmt.Fprintln(&b, "}")
	fmt.Fprintln(&b)
	fmt.Fprintln(&b, "func goString(p *byte) string {")
	fmt.Fprintln(&b, "\tif p == nil {")
	fmt.Fprintln(&b, "\t\treturn \"\"")
	fmt.Fprintln(&b, "\t}")
	fmt.Fprintln(&b, "\tn := 0")
	fmt.Fprintln(&b, "\tfor *(*byte)(unsafe.Add(unsafe.Pointer(p), n)) != 0 {")
	fmt.Fprintln(&b, "\t\tn++")
	fmt.Fprintln(&b, "\t}")
	fmt.Fprintln(&b, "\treturn string(unsafe.Slice(p, n))")
	fmt.Fprintln(&b, "}")
	fmt.Fprintln(&b)
	fmt.Fprintln(&b, "func bytesPointer(b []byte) unsafe.Pointer {")
	fmt.Fprintln(&b, "\tif len(b) == 0 {")
	fmt.Fprintln(&b, "\t\treturn nil")
	fmt.Fprintln(&b, "\t}")
	fmt.Fprintln(&b, "\treturn unsafe.Pointer(&b[0])")
	fmt.Fprintln(&b, "}")
	fmt.Fprintln(&b)
	fmt.Fprintln(&b, "func allGatherBytesLen(size, elemLen int) (int, error) {")
	fmt.Fprintln(&b, "\tif size < 0 {")
	fmt.Fprintf(&b, "\t\treturn 0, fmt.Errorf(\"all gather: group size %%d is negative\", size)\n")
	fmt.Fprintln(&b, "\t}")
	fmt.Fprintln(&b, "\tif elemLen < 0 {")
	fmt.Fprintf(&b, "\t\treturn 0, fmt.Errorf(\"all gather: input length %%d is negative\", elemLen)\n")
	fmt.Fprintln(&b, "\t}")
	fmt.Fprintln(&b, "\tmax := int(^uint(0) >> 1)")
	fmt.Fprintln(&b, "\tif elemLen != 0 && size > max/elemLen {")
	fmt.Fprintf(&b, "\t\treturn 0, fmt.Errorf(\"all gather: output length overflows int for group size %%d and input length %%d\", size, elemLen)\n")
	fmt.Fprintln(&b, "\t}")
	fmt.Fprintln(&b, "\treturn size * elemLen, nil")
	fmt.Fprintln(&b, "}")
	fmt.Fprintln(&b)
	fmt.Fprintln(&b, "// CError reports an error returned by libjacclc.")
	fmt.Fprintln(&b, "type CError struct {")
	fmt.Fprintln(&b, "\tCall string")
	fmt.Fprintln(&b, "\tMsg  string")
	fmt.Fprintln(&b, "}")
	fmt.Fprintln(&b)
	fmt.Fprintln(&b, "func (e CError) Error() string {")
	fmt.Fprintln(&b, "\tif e.Msg == \"\" {")
	fmt.Fprintf(&b, "\t\treturn fmt.Sprintf(\"%%s failed\", e.Call)\n")
	fmt.Fprintln(&b, "\t}")
	fmt.Fprintf(&b, "\treturn fmt.Sprintf(\"%%s: %%s\", e.Call, e.Msg)\n")
	fmt.Fprintln(&b, "}")
	fmt.Fprintln(&b)
	fmt.Fprintln(&b, "func lastCError(name string) error {")
	fmt.Fprintln(&b, "\tmsg := goString(_mlx_jaccl_last_error())")
	fmt.Fprintln(&b, "\treturn CError{Call: name, Msg: msg}")
	fmt.Fprintln(&b, "}")
	fmt.Fprintln(&b)
	fmt.Fprintln(&b, "func lastCErrorIfAny(name string) error {")
	fmt.Fprintln(&b, "\tmsg := goString(_mlx_jaccl_last_error())")
	fmt.Fprintln(&b, "\tif msg == \"\" {")
	fmt.Fprintln(&b, "\t\treturn nil")
	fmt.Fprintln(&b, "\t}")
	fmt.Fprintln(&b, "\treturn CError{Call: name, Msg: msg}")
	fmt.Fprintln(&b, "}")
	fmt.Fprintln(&b)
	fmt.Fprintln(&b, "func statusError(name string, status int32) error {")
	fmt.Fprintln(&b, "\tif status == 0 {")
	fmt.Fprintln(&b, "\t\treturn nil")
	fmt.Fprintln(&b, "\t}")
	fmt.Fprintln(&b, "\treturn lastCError(name)")
	fmt.Fprintln(&b, "}")
	fmt.Fprintln(&b)
	fmt.Fprintln(&b, "func boolUintptr(v bool) uintptr {")
	fmt.Fprintln(&b, "\tif v {")
	fmt.Fprintln(&b, "\t\treturn 1")
	fmt.Fprintln(&b, "\t}")
	fmt.Fprintln(&b, "\treturn 0")
	fmt.Fprintln(&b, "}")
	fmt.Fprintln(&b)

	for _, fn := range functions {
		writeWrapper(&b, fn)
	}
	writeConvenience(&b, functions)
